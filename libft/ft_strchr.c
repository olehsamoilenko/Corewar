#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int			i;
	char		*str;

	if (s == NULL)
		return (NULL);
	str = (char *)s;
	i = 0;
	while (str[i])
	{
		if (str[i] == (char)c)
			return (&str[i]);
		i++;
	}
	if (str[i] == (char)c)
		return (&str[i]);
	else
		return (0);
}
